public class Information {
    private int idCard;
    private int tel;

    public Information(int idCard, int tel){
        this.idCard = idCard;
        this.tel = tel;
    }

    public void setIdCard(int idCard){
        this.idCard = idCard;
    }
    public int getIdCart(){
        return idCard;
    }

    public void setTel(int tel){
        this.tel = tel;
    }
    public int getTel(){
        return tel;
    }
}
